

public class formatSpecifiersInJava {
    
    public static void main(String[] args) {

        System.out.printf("The sum of %d and %d is : %d %n",2,2,2+2);
        
        System.out.printf("My name is %s %n","Amrit poudel");

        float num = 213.2457f;

        System.out.printf("Round to 3 decimal : %.3f %n",num);

        System.out.printf("PI round to 3 decimal : %.3f %n",Math.PI);


    }
    
}
